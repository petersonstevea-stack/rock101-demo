import type { Instrument } from "./rock101Curriculum";

export type MethodLessonCategory =
  | "technique"
  | "theory"
  | "reading"
  | "rhythm"
  | "earTraining"
  | "songPrep"
  | "performance"
  | "warmup"
  | "other";

export type MethodLesson = {
  id: string;
  instrument: Instrument;
  order: number;
  title: string;
  description?: string;
  category: MethodLessonCategory;
  skillGroup: string;
  externalId?: string;
  isActive?: boolean;
};

type LessonSeed = Omit<MethodLesson, "id" | "instrument" | "order"> & {
  slug: string;
};

function makeLessonId(instrument: Instrument, order: number, slug: string) {
  return `method-${instrument}-${String(order).padStart(3, "0")}-${slug}`;
}

function buildLessons(
  instrument: Instrument,
  lessons: LessonSeed[]
): MethodLesson[] {
  return lessons.map((lesson, index) => ({
    id: makeLessonId(instrument, index + 1, lesson.slug),
    instrument,
    order: index + 1,
    title: lesson.title,
    description: lesson.description,
    category: lesson.category,
    skillGroup: lesson.skillGroup,
    externalId: lesson.externalId,
    isActive: lesson.isActive ?? true,
  }));
}

